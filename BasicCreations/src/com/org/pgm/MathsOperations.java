package com.org.pgm;

public class MathsOperations {
 
	public void add()
	{
		int p,q,r;
		p=20;
		q=30;
		r=p+q;
		System.out.println("Addition : "+r);
	}
	public void sub()
	{
		int p,q,r;
		p=20;
		q=30;
		r=p-q;
		System.out.println("Subtraction: "+r);
	}
	public void mul()
	{
		int p,q,r;
		p=20;
		q=30;
		r=p*q;
		System.out.println("Multiplication : "+r);
	}
	public void div()
	{
		int p,q,r;
		p=20;
		q=30;
		r=p/q;
		System.out.println("Division : "+r);
	}
}
